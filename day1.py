# Downloading Python

# Python : https://www.python.org/downloads/
# Official Documentation : https://docs.python.org/3/

# Step 1) Visit Official Site: Go to https://www.python.org/.
# Step 2) Choose Version: Pick the latest Python version.
# Step 3) Select OS: Click on the link for your operating system (Windows/macOS/Linux).
# Step 4) Download Installer: Get the installer (exe/dmg) file.
# Step 5) Run Installer: Double-click to run it and follow instructions.
# Step 6) Add to PATH (Windows): Check "Add Python to PATH".
# Step 7) Verify: Open terminal/Command Prompt, type python --version.

